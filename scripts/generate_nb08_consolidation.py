# ── Cell 2 — Helper functions (vectorized) ────────────────────────────────────
cells.append(nbf.v4.new_code_cell(
    "def get_country_folders(data_dir):\n"
    "    return sorted([\n"
    "        d for d in os.listdir(data_dir)\n"
    "        if os.path.isdir(os.path.join(data_dir, d))\n"
    "        and not d.startswith((\"Y\", \"Z\"))\n"
    "    ])\n"
    "\n"
    "\n"
    "def load_wosis(country_folder):\n"
    "    \"\"\"Load output_data_points.csv for a country.\"\"\"\n"
    "    path = os.path.join(country_folder, \"output_data_points.csv\")\n"
    "    if not os.path.exists(path):\n"
    "        return None\n"
    "    df = pd.read_csv(path)\n"
    "    df[\"country\"] = os.path.basename(country_folder)\n"
    "    return df\n"
    "\n"
    "\n"
    "def extract_sg_vectorized(tif_path, lats, lons):\n"
    "    \"\"\"\n"
    "    Vectorized extraction: read the entire raster array once,\n"
    "    then index all (lat, lon) pairs at once.\n"
    "    Returns numpy array of float values (NaN where out-of-bounds or NoData).\n"
    "    \"\"\"\n"
    "    try:\n"
    "        with rasterio.open(tif_path) as src:\n"
    "            arr = src.read(1).astype(float)\n"
    "            nodata = src.nodata\n"
    "            transform = src.transform\n"
    "            h, w = src.height, src.width\n"
    "\n"
    "        rows_arr, cols_arr = rowcol(transform, lons, lats)\n"
    "        rows_arr = np.asarray(rows_arr, dtype=int)\n"
    "        cols_arr = np.asarray(cols_arr, dtype=int)\n"
    "\n"
    "        valid = (rows_arr >= 0) & (rows_arr < h) & (cols_arr >= 0) & (cols_arr < w)\n"
    "        vals = np.full(len(lats), np.nan)\n"
    "        vals[valid] = arr[rows_arr[valid], cols_arr[valid]]\n"
    "        if nodata is not None:\n"
    "            vals[vals == nodata] = np.nan\n"
    "        return vals\n"
    "    except Exception:\n"
    "        return np.full(len(lats), np.nan)\n"
    "\n"
    "\n"
    "def get_sg_0_30cm_average_batch(sg_folder, sg_prop, lats, lons):\n"
    "    \"\"\"\n"
    "    Compute weighted average of SoilGrids property over 0-30cm\n"
    "    for all stations at once (vectorized).\n"
    "    Returns numpy array of float values.\n"
    "    \"\"\"\n"
    "    accum = np.zeros(len(lats))\n"
    "    weight_accum = np.zeros(len(lats))\n"
    "\n"
    "    for depth, w in zip(SG_DEPTHS, SG_WEIGHTS):\n"
    "        tif = os.path.join(sg_folder, f\"{sg_prop}_{depth}_mean.tif\")\n"
    "        if not os.path.exists(tif):\n"
    "            continue\n"
    "        vals = extract_sg_vectorized(tif, lats, lons)\n"
    "        good = ~np.isnan(vals)\n"
    "        accum[good] += vals[good] * w\n"
    "        weight_accum[good] += w\n"
    "\n"
    "    result = np.full(len(lats), np.nan)\n"
    "    has_data = weight_accum > 0\n"
    "    divisor = SG_DIVISORS.get(sg_prop, 1)\n"
    "    result[has_data] = np.round(accum[has_data] / weight_accum[has_data] / divisor, 4)\n"
    "    return result\n"
    "\n"
    "\n"
    "def get_available_sg_properties(sg_folder):\n"
    "    \"\"\"List unique SoilGrids property names available in the folder.\"\"\"\n"
    "    tifs = glob.glob(os.path.join(sg_folder, \"*.tif\"))\n"
    "    props = sorted(set(os.path.basename(t).split(\"_\")[0] for t in tifs))\n"
    "    return props\n"
    "\n"
    "\n"
    "print(\"Helper functions defined (vectorized mode).\")"
))

