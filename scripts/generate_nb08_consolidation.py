    "print(f\"Countries: {wosis_df['country'].nunique()}\")\n"
    "print(f\"Total unique stations: {wosis_df.groupby(['lat','lon','country']).ngroups:,}\")\n"
    "print(f\"Properties: {sorted(wosis_df['property'].unique().tolist())}\")\n"
    "print(f\"\\nIssues found: {len(issues)}\")\n"
    "for iss in issues:\n"
