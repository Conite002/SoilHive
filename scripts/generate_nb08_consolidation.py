    "wosis_wide = (\n"
    "    wosis_0_30\n"
    "    .groupby([\"country\", \"lat\", \"lon\", \"property\"])[\"value\"]\n"
    "    .mean()\n"
    "    .unstack(\"property\")\n"
    "    .reset_index()\n"
    ")\n"
    "wosis_wide.columns.name = None\n"
    "\n"
    "print(f\"WoSIS wide (0-30cm): {wosis_wide.shape}\")\n"
    "print(f\"Columns: {wosis_wide.columns.tolist()[:15]}...\")\n"
    "\n"
    "# Merge with SoilGrids\n"
    "if len(sg_df) > 0:\n"
    "    merged = wosis_wide.merge(sg_df, on=[\"country\",\"lat\",\"lon\"], how=\"left\")\n"
    "else:\n"
    "    merged = wosis_wide.copy()\n"
    "print(f\"\\nMerged dataset: {merged.shape}\")\n"
    "\n"
    "# Missing values report\n"
    "prop_cols = [col for col in wosis_wide.columns if col not in [\"country\",\"lat\",\"lon\"]]\n"
    "print(f\"\\nWoSIS property coverage (0-30cm stations):\")\n"
    "for col in prop_cols:\n"
    "    if col in merged.columns:\n"
    "        n = merged[col].notna().sum()\n"
    "        pct = n / len(merged) * 100\n"
    "        print(f\"  {col:15s}: {n:5d}/{len(merged)} ({pct:.1f}%)\")\n"
    "\n"
    "print(f\"\\nStations with ALL WoSIS props missing (potential problem): \", end=\"\")\n"
    "empty = merged[prop_cols].isna().all(axis=1).sum()\n"
    "print(f\"{empty} ({empty/len(merged)*100:.1f}%)\")"
